# -*- coding: utf-8 -*-
"""numbers pipeline: evaluation CSVs -> LaTeX table fragments.
Reads  D:/new/analysis/out/<S>/<S>_metrics.csv  (EOGS-family grid)
       D:/new/analysis/out_cross/<S>/<S>_metrics.csv (cross-method grid)
Writes D:/new/latex/tables/*.tex  -- never hand-edit those files.
Rerun after every evaluation refresh.
"""
import csv
import re
import statistics as st
from pathlib import Path

BASE = Path(r"D:/new/analysis")
OUTDIR = Path(r"D:/new/latex/tables")
OUTDIR.mkdir(parents=True, exist_ok=True)
SCENES = ["JAX_260", "JAX_214", "JAX_004", "JAX_068", "IARPA_001", "IARPA_002", "IARPA_003"]
M = {"overall": "overall_mae", "water": "water_cls9_mae", "building": "building_cls6_mae",
     "weak": "weak_proxy_top20_mae", "err20": "phase0_error_top20_eval_only_mae"}


def load(path):
    if not path.exists():
        return {}
    rows = {}
    with open(path, newline="", encoding="utf-8") as f:
        for r in csv.DictReader(f):
            rows[r["method"]] = {k: (float(r[v]) if r.get(v) not in (None, "", "None") else None)
                                 for k, v in M.items()}
    return rows


def tex_escape(s):
    return s.replace("_", r"\_")


def f3(x):
    return "---" if x is None else f"{x:.3f}"


def main():
    data = {s: load(BASE / "out" / s / f"{s}_metrics.csv") for s in SCENES}
    cross = {s: load(BASE / "out_cross" / s / f"{s}_metrics.csv") for s in SCENES}

    # ---- T1: baseline seed variance ----
    L = [r"% AUTO-GENERATED by gen_latex_tables.py -- do not edit",
         r"\begin{tabular}{lcccc}", r"\toprule",
         r"AOI & $n$ & overall & water & weak proxy \\", r"\midrule"]
    for s in SCENES:
        fam = {k: v for k, v in data[s].items() if re.fullmatch(r"baseline_s\d+", k)}
        if not fam:
            continue
        cells = []
        for key in ("overall", "water", "weak"):
            vals = [v[key] for v in fam.values() if v[key] is not None]
            if not vals:
                cells.append("---")
            else:
                mu = st.mean(vals)
                sd = st.stdev(vals) if len(vals) > 1 else 0.0
                cells.append(f"{mu:.3f}$\\pm${sd:.3f}")
        L.append(f"{tex_escape(s)} & {len(fam)} & " + " & ".join(cells) + r" \\")
    L += [r"\bottomrule", r"\end{tabular}"]
    (OUTDIR / "t1_variance.tex").write_text("\n".join(L), encoding="utf-8")

    # ---- T2: paired deltas ----
    L = [r"% AUTO-GENERATED -- do not edit",
         r"\begin{tabular}{llrrl}", r"\toprule",
         r"AOI & metric & mean $\Delta$ & std & improved \\", r"\midrule"]
    for s in SCENES:
        seeds = sorted({int(k.split("_s")[1]) for k in data[s] if re.fullmatch(r"baseline_s\d+", k)})
        first = True
        for key in ("overall", "water", "weak", "building"):
            ds = []
            for k in seeds:
                b = data[s].get(f"baseline_s{k}", {}).get(key)
                o = data[s].get(f"ours_s{k}", {}).get(key)
                if b is not None and o is not None:
                    ds.append(o - b)
            if not ds:
                continue
            mu = st.mean(ds)
            sd = st.stdev(ds) if len(ds) > 1 else 0.0
            imp = sum(1 for v in ds if v < 0)
            scene_cell = tex_escape(s) if first else ""
            first = False
            L.append(f"{scene_cell} & {key} & {mu:+.3f} & {sd:.3f} & {imp}/{len(ds)} \\\\")
        if not first:
            L.append(r"\addlinespace[2pt]")
    L += [r"\bottomrule", r"\end{tabular}"]
    (OUTDIR / "t2_paired.tex").write_text("\n".join(L), encoding="utf-8")

    # ---- T3: ablation (seed 1337) ----
    L = [r"% AUTO-GENERATED -- do not edit",
         r"\begin{tabular}{llccccc}", r"\toprule",
         r"AOI & config & overall & water & building & weak & err.top20 \\", r"\midrule"]
    for s in SCENES:
        first = True
        for cfg, lab in (("baseline_s1337", "baseline"), ("nostruct_s1337", "no protection"),
                         ("ours_s1337", "protected")):
            if cfg not in data[s]:
                continue
            v = data[s][cfg]
            scene_cell = tex_escape(s) if first else ""
            first = False
            L.append(f"{scene_cell} & {lab} & {f3(v['overall'])} & {f3(v['water'])} & "
                     f"{f3(v['building'])} & {f3(v['weak'])} & {f3(v['err20'])} \\\\")
        if not first:
            L.append(r"\addlinespace[2pt]")
    L += [r"\bottomrule", r"\end{tabular}"]
    (OUTDIR / "t3_ablation.tex").write_text("\n".join(L), encoding="utf-8")

    # ---- T4: cross-method ----
    L = [r"% AUTO-GENERATED -- do not edit",
         r"\begin{tabular}{llccccc}", r"\toprule",
         r"AOI & method & overall & water & building & weak & err.top20 \\", r"\midrule"]
    NAME = {"baseline_s1337": "EOGS++ (s1337)", "eogsv1": "EOGS",
            "satnerf_ds": "Sat-NeRF+DS ckpt", "satnerf": "Sat-NeRF ckpt"}
    for s in SCENES:
        if not cross[s]:
            continue
        first = True
        for meth in ("eogsv1", "baseline_s1337", "satnerf_ds", "satnerf"):
            if meth not in cross[s]:
                continue
            v = cross[s][meth]
            scene_cell = tex_escape(s) if first else ""
            first = False
            L.append(f"{scene_cell} & {NAME[meth]} & {f3(v['overall'])} & {f3(v['water'])} & "
                     f"{f3(v['building'])} & {f3(v['weak'])} & {f3(v['err20'])} \\\\")
        fam = {k: v for k, v in data[s].items() if re.fullmatch(r"baseline_s\d+", k)}
        if fam:
            cells = []
            for key in ("overall", "water", "building", "weak", "err20"):
                vals = [v[key] for v in fam.values() if v[key] is not None]
                cells.append("---" if not vals else
                             f"{st.mean(vals):.3f}$\\pm${(st.stdev(vals) if len(vals) > 1 else 0):.3f}")
            L.append(f" & EOGS++ full-grid ({len(fam)} seeds) & " + " & ".join(cells) + r" \\")
        L.append(r"\addlinespace[2pt]")
    L += [r"\bottomrule", r"\end{tabular}"]
    (OUTDIR / "t4_crossmethod.tex").write_text("\n".join(L), encoding="utf-8")

    print("WROTE", [p.name for p in OUTDIR.glob("*.tex")])


if __name__ == "__main__":
    main()
