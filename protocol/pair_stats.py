#!/usr/bin/env python3
"""Two-sample stats for the published pair EOGS -> EOGS++ on the pair grid,
plus the one-factor TSDF arm (EOGS++ with TSDF extracted from the SAME
checkpoints, seed-paired against EOGS++ without TSDF).

Reads D:/new/analysis/out_pair/<S>/<S>_metrics.csv
(6 eogspp_* + 6 eogsv1_* + 6 eogspp_tsdf_* rows per scene).
Emits latex/tables/t5_pair.tex and console numbers for Sec. VI.
"""
import re
from pathlib import Path

import numpy as np
import pandas as pd
from scipy import stats

BASE = Path("D:/new/analysis")
OUTDIR = Path("D:/new/latex/tables")
SCENES = ["JAX_260", "JAX_214", "JAX_004", "JAX_068", "IARPA_001", "IARPA_002", "IARPA_003"]
REGIONS = [("overall", "overall_mae"), ("water", "water_cls9_mae"),
           ("building", "building_cls6_mae"), ("weak", "weak_proxy_top20_mae")]
SEEDS = [1337, 2024, 3407, 5150, 6001, 7777]
MIN_PX = 500


def load(s):
    df = pd.read_csv(BASE / "out_pair" / s / f"{s}_metrics.csv").set_index("method")
    return df


def fmt_p(p):
    return "<0.001" if p < 0.001 else f"{p:.3f}" if p < 0.01 else f"{p:.2f}"


def main():
    def star(p):
        return "" if p >= 0.05 else (r"$^{**}$" if p < 0.001 else r"$^{*}$")

    lines = ["% AUTO-GENERATED by pair_stats.py -- do not edit",
             r"\begin{tabular}{llrrrr}", r"\toprule",
             r"AOI & region & EOGS & EOGS++ & $\Delta$ & $\Delta_{\mathrm{TSDF}}$ \\",
             r"\midrule"]
    sc_deltas = {r: [] for r, _ in REGIONS}
    all_p = []
    tsdf_dirs = []
    for s in SCENES:
        df = load(s)
        v1 = df[df.index.str.match(r"eogsv1(_default|_s\d+)")]
        pp = df[df.index.str.match(r"eogspp_s\d+")]
        ts = df[df.index.str.match(r"eogspp_tsdf_s\d+")]
        first = True
        for rname, col in REGIONS:
            pxcol = col.replace("_mae", "_pixels")
            if col not in df.columns or pp[col].isna().all() or v1[col].isna().all():
                continue
            if pxcol in df.columns and df[pxcol].dropna().min() < MIN_PX:
                continue
            a, b = v1[col].dropna().values, pp[col].dropna().values
            if len(a) < 2 or len(b) < 2:
                continue
            d = b.mean() - a.mean()
            t, p = stats.ttest_ind(b, a, equal_var=False)
            sc_deltas[rname].append((s, d))
            all_p.append((s, rname, d, p))
            # one-factor TSDF: paired per-seed (tsdf - plain) within EOGS++
            tc = ""
            if len(ts) >= 3:
                pairs = []
                for k in SEEDS:
                    r1, r2 = f"eogspp_s{k}", f"eogspp_tsdf_s{k}"
                    if r1 in df.index and r2 in df.index:
                        x, y = df.loc[r1, col], df.loc[r2, col]
                        if np.isfinite(x) and np.isfinite(y):
                            pairs.append(y - x)
                if len(pairs) >= 3:
                    dt = float(np.mean(pairs))
                    _, pt = stats.ttest_1samp(pairs, 0.0)
                    tc = f"{dt:+.3f}{star(pt)}"
                    if rname == "building":
                        dd = ts[col].dropna().mean() - a.mean()
                        tsdf_dirs.append((s, round(dt, 3), round(pt, 4), round(dd, 3)))
            cell0 = s.replace("_", r"\_") if first else ""
            first = False
            lines.append(f"{cell0} & {rname} & {a.mean():.3f}$\\pm${a.std(ddof=1):.3f}"
                         f" & {b.mean():.3f}$\\pm${b.std(ddof=1):.3f}"
                         f" & {d:+.3f}{star(p)} & {tc} \\\\")
            print(f"{s:10s} {rname:9s} v1={a.mean():.3f}+-{a.std(ddof=1):.3f} "
                  f"pp={b.mean():.3f}+-{b.std(ddof=1):.3f} d={d:+.3f} p={p:.4f} tsdf[{tc}]")
        lines.append(r"\addlinespace[2pt]")
    lines += [r"\bottomrule", r"\end{tabular}"]
    (OUTDIR / "t5_pair.tex").write_text("\n".join(lines), encoding="utf-8")
    print("\nWROTE", OUTDIR / "t5_pair.tex")

    print("\n=== significance bookkeeping (v1 vs pp) ===")
    sig = [(s, r, d, p) for s, r, d, p in all_p if p < 0.05]
    print(f"cells: {len(all_p)}, significant@0.05: {len(sig)}")
    ps = sorted(p for _, _, _, p in all_p)
    m = len(ps)
    holm = 0
    for i, p in enumerate(ps):
        if p <= 0.05 / (m - i):
            holm += 1
        else:
            break
    print(f"Holm survivors: {holm} of {len(sig)}")
    mx = max(all_p, key=lambda x: abs(x[2]))
    print("max |d| cell:", mx)

    print("\n=== across-scene Wilcoxon on scene deltas (pp - v1) ===")
    for rname, _ in REGIONS:
        ds = [d for _, d in sc_deltas[rname]]
        if len(ds) >= 5:
            w = stats.wilcoxon(ds)
            print(f"{rname:9s} n={len(ds)} deltas={[round(x, 3) for x in ds]} p={w.pvalue:.3f}")
        else:
            print(f"{rname:9s} n={len(ds)} deltas={[round(x, 3) for x in ds]} (no test)")

    print("\n=== 7-scene means: EOGS / EOGS++ / EOGS++(TSDF) ===")
    for rname, col in REGIONS:
        va, vb, vc = [], [], []
        for s in SCENES:
            df = load(s)
            pxcol = col.replace("_mae", "_pixels")
            if pxcol in df.columns and df[pxcol].dropna().min() < MIN_PX:
                continue
            va.append(df[df.index.str.match(r"eogsv1")][col].dropna().mean())
            vb.append(df[df.index.str.match(r"eogspp_s\d+")][col].dropna().mean())
            vc.append(df[df.index.str.match(r"eogspp_tsdf")][col].dropna().mean())
        print(f"{rname}: n={len(va)} EOGS {np.mean(va):.3f}  EOGS++ {np.mean(vb):.3f}  "
              f"EOGS++TSDF {np.nanmean(vc):.3f}")

    print("\n=== TSDF one-factor on building (paired dt, p, and tsdf-vs-v1 direction) ===")
    for row in tsdf_dirs:
        print(row)

    print("\n=== EOGS-v1 overall seed range per scene ===")
    for s in SCENES:
        df = load(s)
        v1 = df[df.index.str.match(r"eogsv1")]["overall_mae"].dropna()
        print(f"{s}: range {v1.max() - v1.min():.3f} std {v1.std(ddof=1):.3f}")


if __name__ == "__main__":
    main()
