#!/usr/bin/env python3
"""Two-sample stats for the published pair EOGS -> EOGS++ on the pair grid.

Reads D:/new/analysis/out_pair/<S>/<S>_metrics.csv (6 eogspp_* + 6 eogsv1_* rows).
Cross-codebase => no seed pairing: Welch t per scene-region; Wilcoxon across scenes.
Emits a LaTeX mini-table (t5_pair.tex) and console numbers for Sec. VI.
"""
import re
from pathlib import Path

import numpy as np
import pandas as pd
from scipy import stats

BASE = Path("D:/new/analysis")
OUTDIR = Path("D:/new/latex/tables")
SCENES = ["JAX_260", "JAX_214", "JAX_004", "JAX_068", "IARPA_001", "IARPA_002", "IARPA_003"]
REGIONS = [("overall", "overall_mae"), ("water", "water_cls9_mae"),
           ("building", "building_cls6_mae"), ("weak", "weak_proxy_top20_mae")]


def load(s):
    df = pd.read_csv(BASE / "out_pair" / s / f"{s}_metrics.csv").set_index("method")
    pp = df[df.index.str.startswith("eogspp")]
    v1 = df[df.index.str.startswith("eogsv1")]
    return pp, v1


def fmt_p(p):
    return "<0.001" if p < 0.001 else f"{p:.3f}" if p < 0.01 else f"{p:.2f}"


def main():
    lines = ["% AUTO-GENERATED by pair_stats.py -- do not edit",
             r"\begin{tabular}{llrrrl}", r"\toprule",
             r"AOI & region & EOGS & EOGS++ & $\Delta$ & $p$ \\", r"\midrule"]
    sc_deltas = {r: [] for r, _ in REGIONS}
    for s in SCENES:
        pp, v1 = load(s)
        first = True
        for rname, col in REGIONS:
            if col not in pp.columns or pp[col].isna().all() or v1[col].isna().all():
                continue
            a, b = v1[col].dropna().values, pp[col].dropna().values
            if len(a) < 2 or len(b) < 2:
                continue
            d = b.mean() - a.mean()
            t, p = stats.ttest_ind(b, a, equal_var=False)
            sc_deltas[rname].append(d)
            cell0 = s.replace("_", r"\_") if first else ""
            first = False
            lines.append(f"{cell0} & {rname} & {a.mean():.3f}$\\pm${a.std(ddof=1):.3f}"
                         f" & {b.mean():.3f}$\\pm${b.std(ddof=1):.3f}"
                         f" & {d:+.3f} & {fmt_p(p)} \\\\")
            print(f"{s:10s} {rname:9s} v1={a.mean():.3f}±{a.std(ddof=1):.3f} "
                  f"pp={b.mean():.3f}±{b.std(ddof=1):.3f} d={d:+.3f} p={p:.4f} "
                  f"ratio={abs(d)/max(a.std(ddof=1), b.std(ddof=1)):.1f}x")
        lines.append(r"\addlinespace[2pt]")
    lines += [r"\bottomrule", r"\end{tabular}"]
    (OUTDIR / "t5_pair.tex").write_text("\n".join(lines), encoding="utf-8")
    print("\nWROTE", OUTDIR / "t5_pair.tex")
    print("\n=== across-scene Wilcoxon on scene deltas (EOGS++ - EOGS) ===")
    for rname, _ in REGIONS:
        ds = sc_deltas[rname]
        if len(ds) >= 5:
            w = stats.wilcoxon(ds)
            print(f"{rname:9s} n={len(ds)} deltas={[round(x,3) for x in ds]} p={w.pvalue:.3f}")
        else:
            print(f"{rname:9s} n={len(ds)} deltas={[round(x,3) for x in ds]} (no test)")
    print("\n=== 7-scene mean building (their claim: 1.33 -> 1.19) ===")
    for rname in ["building", "overall"]:
        col = dict(REGIONS)[rname]
        va = [load(s)[1][col].dropna().mean() for s in SCENES]
        vb = [load(s)[0][col].dropna().mean() for s in SCENES]
        print(f"{rname}: EOGS {np.mean(va):.3f}  EOGS++ {np.mean(vb):.3f}")
    print("\n=== EOGS-v1 seed range (for 'shifts by up to X m' sentence) ===")
    for s in SCENES:
        pp, v1 = load(s)
        rng = v1["overall_mae"].max() - v1["overall_mae"].min()
        print(f"{s}: overall range {rng:.3f}  std {v1['overall_mae'].std(ddof=1):.3f}")


if __name__ == "__main__":
    main()
